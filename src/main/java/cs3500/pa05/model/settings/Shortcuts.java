package cs3500.pa05.model.settings;

/**
 * Represents the available shortcuts.
 */
public enum Shortcuts {
  /**
   * Shortcut to create a new event
   */
  CREATE_EVENT,
  /**
   * Shortcut to create a new task
   */
  CREATE_TASK,
  /**
   * Shortcut to save bullet journal
   */
  SAVE,
  /**
   * Shortcut to open a saved bullet journal
   */
  OPEN,
  /**
   * Shortcut to create a new week
   */
  NEW_WEEK,
  /**
   * Shortcut to change theme to light mode
   */
  CHANGE_TO_LIGHT,
  /**
   * Shortcut to change theme to dark mode
   */
  CHANGE_TO_DARK
}

